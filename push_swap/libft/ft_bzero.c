#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t			i;
	char			*st;

	if (n != 0)
	{
		st = (char *)s;
		i = 0;
		while (i < n)
		{
			st[i] = 0;
			i++;
		}
	}
}
